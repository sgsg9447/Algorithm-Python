# #전위순회

def DFS(v):
    if v>7:
        return
    else: #호출 넘어가기전에 본연의 일을 다 해놓고 넘어감: 전위순회! 자기껄 먼저하고 왼쪽오른쪽감!
        print(v, end = ' ')
        DFS(v*2)
        DFS(v*2+1)


if __name__ == "__main__":
    DFS(1) 

