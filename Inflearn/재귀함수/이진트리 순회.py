# #중위순회

def DFS(v): 
    if v>7:
        return
    else: #왼쪽 먼저 처리하고, 부모가 중간에서 처리되는거지! 중위순회
        DFS(v*2)
        print(v, end = ' ')
        DFS(v*2+1)

if __name__ == "__main__":
    DFS(1) 

